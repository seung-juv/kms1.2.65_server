/*
 * NPCID : 2141002
 * ScriptName : PinkBeen_Out
 * NPCNameFunc : 잊혀진 신전관리인
 * Location : 270050200 (신전 깊은 곳 - 잃어버린 황혼)
 * Location : 270050100 (신전 깊은 곳 - 신들의 황혼)
 * 
 * @author T-Sun
 *
 */

var status = -1;
function action(mode, type, selection) {
    if (mode == 1 && type != 1) {
        status++;
    } else {
        if (type == 1 && mode == 1) {
            status++;
            selection = 1;
        } else if (type == 1 && mode == 0) {
            status++;
            selection = 0;
        } else {
            cm.dispose();
            return;
        }
    }
    if (status == 0) {
        cm.sendYesNo("정말 이곳에서 나가 안전한 곳으로 돌아가고 싶으십니까?")
    } else if (status == 1) {
        cm.warp(270050300);
        cm.dispose();
    }
}