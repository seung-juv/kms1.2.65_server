/*
 * Quest ID : 9251
 * Quest Name : 페티트의 메이플 메이트
 * Quest Progress Info : 메이플 메이트 서비스를 위해 핸드폰 인증을 준비할 테니, 잠시 후 다시 말을 걸어 달라고 했다.
 * Quest End Info : 휴대폰을 인증하는데 성공했다. 다시 인증하려면 페티트를 찾아가보자.
 * Start NPC : 9010008
 * End NPC : 9010008
 * 
 * @author T-Sun
 *
 */

var status = -1;

function start(mode, type, selection) {
    if (mode == 1 && type != 1 && type != 11) {
        status++;
    } else {
        if ((type == 1 || type == 11) && mode == 1) {
            status++;
            selection = 1;
        } else if ((type == 1 || type == 11) && mode == 0) {
            status++;
            selection = 0;
        } else {
            qm.dispose();
            return;
        }
    }
    if (status == 0) {
        qm.forceStartQuest();
        qm.showQuestClear(9251);
        qm.dispose();
    } 
}
function end(mode, type, selection) {
    if (mode == 1 && type != 1 && type != 11) {
        status++;
    } else {
        if ((type == 1 || type == 11) && mode == 1) {
            status++;
            selection = 1;
        } else if ((type == 1 || type == 11) && mode == 0) {
            status++;
            selection = 0;
        } else {
            qm.dispose();
            return;
        }
    }
    if (status == 0) {
        qm.forceCompleteQuest();
        qm.showQuestCompleteEffect();
        qm.dispose();
    } 
}
