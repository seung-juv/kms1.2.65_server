/*
 * Quest ID : 9252
 * Quest Name : 핸드폰 메이플스토리 도적편
 * Quest Progress Info : 후대폰 메이플스토리 도적편을 다운받았다.
 * Quest End Info : 후대폰 메이플스토리 도적편을 다운받았다.
 * Start NPC : 9010008
 * 
 * @author T-Sun
 *
 */

var status = -1;

function start(mode, type, selection) {
    if (mode == 1 && type != 1 && type != 11) {
        status++;
    } else {
        if ((type == 1 || type == 11) && mode == 1) {
            status++;
            selection = 1;
        } else if ((type == 1 || type == 11) && mode == 0) {
            status++;
            selection = 0;
        } else {
            qm.dispose();
            return;
        }
    }
    if (status == 0) {
        qm.forceStartQuest();
        qm.showQuestClear(9252);
        qm.dispose();
    } 
}
