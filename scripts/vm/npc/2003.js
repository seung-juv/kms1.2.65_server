    self.say("다른 서버에서 보지 못하셨던 각종 파티퀘스트, 그리고 퀘스트. 또한 결혼, 커플링, 우정링, 길드연합, 패밀리 등 각종 커뮤니티 기능 또한 마음대로 즐기실 수 있습니다.");
