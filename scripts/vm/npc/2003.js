        var answer = self.askMenu("더 궁금하신게 있으신가요?#b\r\n #L1#직업은 어디까지 구현되었나요?#l\r\n #L2#금지 스킬이나 직업이 있나요?#l \r\n #L3#파티퀘스트가 정말 다 된다구요?#l\r\n #L4#서버 규칙에 대하여 알려주세요.#l\r\n #L0#더 이상 질문할 게 없어요.#l");
